type DotPrefix<T extends string> = T extends "" ? "" : `.${T}`;
type Paths<T> = T extends object ? {
    [K in keyof T]-?: K extends string ? `${K}${DotPrefix<Paths<T[K]>>}` : never
}[keyof T] : "";

